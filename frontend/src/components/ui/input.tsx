import * as React from "react"

import { cn } from '../../lib/utils'

function Input({ className, type, style, ...props }: React.ComponentProps<"input">) {
  // Default inline style to ensure readable text and dark background
  const defaultStyle: React.CSSProperties = {
    backgroundColor: "#ffffff", // dark slate
    color: "#0f1724", // light text
  }

  return (
    <input
      type={type}
      data-slot="input"
      style={{ ...defaultStyle, ...(style || {}) }}
      className={cn(
        "file:text-foreground placeholder:text-zinc-400 selection:bg-primary selection:text-primary-foreground dark:bg-input/30 border-input flex h-9 w-full min-w-0 rounded-md border px-3 py-1 text-base text-zinc-100 shadow-xs transition-[color,box-shadow] outline-none file:inline-flex file:h-7 file:border-0 file:bg-transparent file:text-sm file:font-medium disabled:pointer-events-none disabled:cursor-not-allowed disabled:opacity-50 md:text-sm",
        "focus-visible:border-ring focus-visible:ring-ring/50 focus-visible:ring-[3px]",
        "aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive",
        className
      )}
      {...props}
    />
  )
}

export { Input }
